#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*copy;
	size_t	len;

	len = ft_strlen(str);
	if (!(copy = (char *)malloc(sizeof(*str) * (len + 1))))
		return (NULL);
	ft_strcpy(copy, str);
	return (copy);
}
